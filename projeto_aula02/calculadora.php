<?php

$contador = 10;

echo "Valor Inicial: " . $contador . "<br>";


$contador += 5; // 
echo "Após += 5: " . $contador . "<br>";

$contador -= 3; // 
echo "Após -= 3: " . $contador . "<br>";

echo "Pós-incremento (12++): " . $contador++ . "<br>";
echo "Valor Atual: " . $contador . "<br>"; 

echo "Pré-incremento (++13): " . ++$contador . "<br>"; 
echo "Valor Final: " . $contador . "<br>"; 

?>
